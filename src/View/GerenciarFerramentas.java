    private void b_alterar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_alterar1ActionPerformed
        // TODO add your handling code here:
        int id = 0;
        String nome = "";
        String marca = "";
        double custo = 0;
        
        try{
            
            if (this.jTableGerenciarFerramentas.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione uma ferramenta para alterar");
            } else {
                id = Integer.parseInt(this.jTableGerenciarFerramentas.getValueAt(this.jTableGerenciarFerramentas.getSelectedRow(), 0).toString());
            }             
            

            if (this.textNome1.getText().length() < 2) {
                throw new Mensagens ("Nome muito curto");
            } else {
                nome = this.textNome1.getText();
            }

            for (char c : nome.toCharArray())
            {
                if (Character.isDigit(c))
                {
                    throw new Mensagens("Nome deve conter apenas letras");
                }
            }

            if (this.textCusto.getText().length() < 0) {       
                throw new Mensagens("Custo deve conter ao menos 3 caracteres.");
            }          
            else {
                custo = Double.parseDouble(this.textCusto.getText());
            }

            if (this.textMarca.getText().length() < 2) {
                throw new Mensagens("Marca muito curta");
            }
            else {
                marca = this.textMarca.getText();
            }
            
            System.out.println(id + nome + marca + custo);
            
            if (this.objetoFerramenta.UpdateFerramentaBD(id, nome, marca, custo)) {
                this.textNome1.setText(""); 
                this.textMarca.setText(""); 
                this.textCusto.setText("");
                JOptionPane.showMessageDialog(rootPane, "Ferramenta alterada com sucesso! ");               
            }
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(rootPane, erro.getMessage());
        } catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, "Insira um valor valido: " + ne.getMessage());
        } finally {
            carregarTabela();
        }   
    }//GEN-LAST:event_b_alterar1ActionPerformed

    private void b_apagar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_apagar1ActionPerformed
        // TODO add your handling code here:
        try {
            int id = 0;
            
            if (this.jTableGerenciarFerramentas.getSelectedRow() == -1) {
                throw new Mensagens("Selecione uma ferramenta para apagar");
            } else {
                id = Integer.parseInt(this.jTableGerenciarFerramentas.getValueAt(this.jTableGerenciarFerramentas.getSelectedRow(), 0).toString());
            }
            
            //int resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja apagar essa ferramenta? ");
            int resposta = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja apagar essa ferramenta?", "Selecione uma opção", WIDTH);
            
            if (resposta == 0) {
                if (this.objetoEmprestimo.setFerramentaIdToNull(id) && this.objetoFerramenta.DeleteFerramentaoBD(id)) {
                    this.textMarca.setText("");
                    this.textNome1.setText("");
                    this.textCusto.setText("");
                    
                    JOptionPane.showMessageDialog(rootPane, "Ferramenta apagada com sucesso!");
                }
            }
                
        } catch(Mensagens e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        } finally {
            carregarTabela();
        }
    }//GEN-LAST:event_b_apagar1ActionPerformed

