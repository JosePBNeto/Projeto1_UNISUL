    @SuppressWarnings("unchecked")
    public void carregarTabela() {
        
        DefaultTableModel model = (DefaultTableModel) this.jTableGerenciarFerramentas.getModel();
        model.setNumRows(0);
        
        ArrayList<Ferramenta> lista = new ArrayList<>();
        
        lista = objetoFerramenta.getMinhaLista();
           
        for (Ferramenta ferramenta : lista) {
            model.addRow(new Object[]{
                ferramenta.getId(),
                ferramenta.getNome(),
                ferramenta.getMarca(),
                ferramenta.getDisponibilidade(),
                ferramenta.getCusto(),

            });
        } 
    }
    
    public void carregarTotal() {
        double total = 0;
        total = this.objetoFerramenta.calcularTotal();
        
        this.jTextTotalGasto.setText("R$ " + Double.toString(total));
    }
